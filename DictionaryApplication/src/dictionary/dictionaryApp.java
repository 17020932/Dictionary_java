    public void showAllWord (){
        readFromFile();
        for(Word element : dictionary_Management.dictionary.getNewWord()){
            listModel.addElement(element.getWord_target());
        }
        listWord.setModel(listModel);
    }

    //tim kiem va loc du lieu
    public void searchFilter(String searchWord) {
        DefaultListModel filteredWord = new DefaultListModel();
        
        dictionary_Management.dictionary.getNewWord().stream().forEach((newWord) -> {
            
            String w = newWord.getWord_target().toLowerCase();
            if(w.indexOf(searchWord.toLowerCase()) == 0) {
                filteredWord.addElement(w);
            }
        });
        
        listModel = filteredWord;
        listWord.setModel(listModel);
    }
    

    //tim kiem nghia cua tu
    public String dictionaryLookUp(String str_find) {
        int index = dictionary_Management.LookUp(str_find);
        if(index < 0)   return "";
        else            return dictionary_Management.dictionary.getNewWord().get(index).getWord_explain();
    }


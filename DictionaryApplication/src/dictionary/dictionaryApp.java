    private void listWordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_listWordKeyPressed
        // TODO add your handling code here:

        int n = listWord.getSelectedIndex();

        //if(n==0)    listWord.setSelectedIndex(n);
        if (evt.getKeyCode() == KeyEvent.VK_UP) {
            if (n > 0) {
                listWord.setSelectedIndex(n--);
            }
        } else if (evt.getKeyCode() == KeyEvent.VK_DOWN) {
            listWord.setSelectedIndex(n++);
        } else if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            searchBar.setText((String) listWord.getSelectedValue());
            //displayExplain.setText(dictionaryLookUp((String) listWord.getSelectedValue()));
            String explain_word = dictionaryLookUp((String) listWord.getSelectedValue());
            String[] part = explain_word.trim().split("<>");
            displayExplain.setText("\n");
            for (int i = 0; i < part.length; i++) {
                displayExplain.append("   " + part[i] + "\n");
            }
        }
        
        
    }//GEN-LAST:event_listWordKeyPressed

